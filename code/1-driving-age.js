function drivingAge() {
  let age = prompt("How old are you?")

  if (age >= 16) {
    alert("You're old enough to drive.")
  }

  if (age < 16) alert("You're not old enough to drive.")
  //this code asks the user for their ages and if they are old enough, it tells them they can drive, if they are not old enough, it tells them they are not old enough to drive
}
